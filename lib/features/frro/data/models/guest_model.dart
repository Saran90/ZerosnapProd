import '../../domain/entities/branch.dart';
import '../../domain/entities/guest.dart';

/// Data model for Guest with JSON serialization
class GuestModel extends Guest {
  const GuestModel({
    required super.guestdataId,
    required super.guestCode,
    required super.firstName,
    required super.lastName,
    required super.phoneNo,
    required super.email,
    required super.gender,
    required super.nationality,
    required super.nationalityText,
    required super.dateOfBirth,
    required super.address,
    required super.country,
    required super.countryText,
    required super.city,
    required super.purposeOfVisit,
    required super.documentNo,
    required super.countryOfIssue,
    required super.countryOfIssueText,
    required super.dateOfIssue,
    required super.expiryDate,
    required super.visaNo,
    required super.visaPOICity,
    required super.visaPOICountry,
    required super.visaDateOfIssue,
    required super.visaValidTill,
    required super.visaType,
    required super.arrivalDate,
    required super.arrivalTime,
    required super.checkOutDate,
    required super.checkOutTime,
    required super.profilePic,
    required super.passToFRRO,
    required super.isCheckOut,
    required super.dateOfArrivalInIndia,
    required super.arrivedFromCountry,
    required super.arrivedFromCity,
    required super.arrivedFromPlace,
    required super.nextDestination,
    required super.nextDestinationInState,
    required super.nextDestinationInDistrict,
    required super.nextDestinationInPlace,
    required super.nextDestinationOutCountry,
    required super.nextDestinationOutCity,
    required super.nextDestinationOutPlace,
    required super.specialCategory,
    required super.visaSubTypeId,
    required super.visaSubTypeName,
    super.branch,
  });

  factory GuestModel.fromJson(Map<String, dynamic> json) {
    return GuestModel(
      guestdataId: json['Guestdata_id'] ?? 0,
      guestCode: json['Guest_Code'] ?? '',
      firstName: json['Guest_Firstname'] ?? '',
      lastName: json['Guest_Lastname'] ?? '',
      phoneNo: json['Guest_PhoneNo'] ?? '',
      email: json['Guest_Email'] ?? '',
      gender: json['Guest_Gender'] ?? '',
      nationality: json['Guest_Nationality'] ?? '',
      nationalityText: json['Guest_NationalityTxt'] ?? '',
      dateOfBirth: json['Guest_DOB'] ?? '',
      address: json['Guest_Address'] ?? '',
      country: json['Guest_Country'] ?? '',
      countryText: json['Guest_CountryTxt'] ?? '',
      city: json['Guest_City'] ?? '',
      purposeOfVisit: json['Guest_PurposeofVisit'] ?? '',
      documentNo: json['Guest_DocumentNo'] ?? '',
      countryOfIssue: json['Guest_CountryofIssue'] ?? '',
      countryOfIssueText: json['Guest_CountryofIssueTxt'] ?? '',
      dateOfIssue: json['Guest_DateOfIssue'] ?? '',
      expiryDate: json['Guest_ExpiryDate'] ?? '',
      visaNo: json['Guest_VisaNo'] ?? '',
      visaPOICity: json['Guest_VisaPOICity'] ?? '',
      visaPOICountry: json['Guest_VisaPOICountry'] ?? '',
      visaDateOfIssue: json['Guest_VisaDateofIssue'] ?? '',
      visaValidTill: json['Guest_VisaValidTill'] ?? '',
      visaType: json['Guest_VisaType'] ?? '',
      arrivalDate: json['Arrival_Date'] ?? '',
      arrivalTime: json['Arrival_Time'] ?? '',
      checkOutDate: json['Guest_HotelCheckOut'] ?? '',
      checkOutTime: json['Guest_HotelCheckOutTime'] ?? '',
      profilePic: json['Guest_ProfilePic'] ?? '',
      passToFRRO: json['Guest_PassToFRRO'] ?? 0,
      isCheckOut: json['IsCheckOut'] ?? 0,
      dateOfArrivalInIndia: json['DateOfArrivalInIndia'] ?? '',
      arrivedFromCountry: json['ArrivedFromCountry'] ?? '',
      arrivedFromCity: json['ArrivedFromCity'] ?? '',
      arrivedFromPlace: json['ArrivedFromPlace'] ?? '',
      nextDestination: json['NextDestination']?.toString() ?? '',
      nextDestinationInState: json['NextDestination_IN_State'] ?? '',
      nextDestinationInDistrict: json['NextDestination_IN_District'] ?? '',
      nextDestinationInPlace: json['NextDestination_IN_Place'] ?? '',
      nextDestinationOutCountry: json['NextDestination_OUT_Country'] ?? '',
      nextDestinationOutCity: json['NextDestination_OUT_City'] ?? '',
      nextDestinationOutPlace: json['NextDestination_OUT_Place'] ?? '',
      specialCategory: json['SpecialCategory'] ?? '',
      visaSubTypeId: json['VisaSubTypeId']?.toString() ?? '',
      visaSubTypeName: json['VisaSubTypeName'] ?? '',
      branch: _parseBranch(json['Branch']),
    );
  }

  static Branch _parseBranch(dynamic b) {
    if (b == null || b is! Map<String, dynamic>) return Branch.empty;
    return Branch(
      name: b['Branch_Name'] ?? '',
      address: b['Branch_Address'] ?? '',
      city: b['Branch_City'] ?? '',
      state: b['Branch_State']?.toString() ?? '',
      district: b['branch_district']?.toString() ?? '',
      pinCode: b['Branch_PinCode'] ?? '',
      phone: b['Branch_Phone'] ?? '',
      email: b['Branch_email'] ?? '',
      addressInIndia: b['AddressInIndia'] ?? '',
      fromGuestAddressInIndia: (b['FromGuestAddressInIndia'] as int?) ?? 0,
      contactPhoneInIndia: b['ContactPhoneInIndia'] ?? '',
      mobileInIndia: b['MobileInIndia'] ?? '',
      contactPhonePermanentlyResiding:
          b['ContactPhonePermanentlyResiding'] ?? '',
      mobilePermanentlyResiding: b['MobilePermanentlyResiding'] ?? '',
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'Guestdata_id': guestdataId,
      'Guest_Code': guestCode,
      'Guest_Firstname': firstName,
      'Guest_Lastname': lastName,
      'Guest_PhoneNo': phoneNo,
      'Guest_Email': email,
      'Guest_Gender': gender,
      'Guest_Nationality': nationality,
      'Guest_NationalityTxt': nationalityText,
      'Guest_DOB': dateOfBirth,
      'Guest_Address': address,
      'Guest_Country': country,
      'Guest_CountryTxt': countryText,
      'Guest_City': city,
      'Guest_PurposeofVisit': purposeOfVisit,
      'Guest_DocumentNo': documentNo,
      'Guest_CountryofIssue': countryOfIssue,
      'Guest_CountryofIssueTxt': countryOfIssueText,
      'Guest_DateOfIssue': dateOfIssue,
      'Guest_ExpiryDate': expiryDate,
      'Guest_VisaNo': visaNo,
      'Guest_VisaPOICity': visaPOICity,
      'Guest_VisaPOICountry': visaPOICountry,
      'Guest_VisaDateofIssue': visaDateOfIssue,
      'Guest_VisaValidTill': visaValidTill,
      'Guest_VisaType': visaType,
      'Arrival_Date': arrivalDate,
      'Arrival_Time': arrivalTime,
      'Guest_HotelCheckOut': checkOutDate,
      'Guest_HotelCheckOutTime': checkOutTime,
      'Guest_ProfilePic': profilePic,
      'Guest_PassToFRRO': passToFRRO,
      'IsCheckOut': isCheckOut,
      'DateOfArrivalInIndia': dateOfArrivalInIndia,
      'ArrivedFromCountry': arrivedFromCountry,
      'ArrivedFromCity': arrivedFromCity,
      'ArrivedFromPlace': arrivedFromPlace,
      'NextDestination': nextDestination,
      'NextDestination_IN_State': nextDestinationInState,
      'NextDestination_IN_District': nextDestinationInDistrict,
      'NextDestination_IN_Place': nextDestinationInPlace,
      'NextDestination_OUT_Country': nextDestinationOutCountry,
      'NextDestination_OUT_City': nextDestinationOutCity,
      'NextDestination_OUT_Place': nextDestinationOutPlace,
      'SpecialCategory': specialCategory,
      'VisaSubTypeId': visaSubTypeId,
      'VisaSubTypeName': visaSubTypeName,
    };
  }
}
